  _getMyUserIds() {
    if (!this.activeUsers || !currentUser.userName) return [currentUser.userId];

    const myName = currentUser.userName;
    const myIds = new Set([currentUser.userId]); // Always include current ID

    // Find all other userIds with the same name from the active list
    for (const userId in this.activeUsers) {
        const user = this.activeUsers[userId];
        if (user && user.userName === myName) {
            myIds.add(userId);
        }
    }
    return Array.from(myIds);
  }

  _guestNameFromId(userId) {
    if (typeof userId !== 'string' || !userId.startsWith('guest_')) return null;
    return `guest-${userId.substring(6, 10)}`;
  }

  _isUnknownName(name) {
    if (typeof name !== 'string') return true;
    const trimmed = name.trim();
    if (!trimmed) return true;
    return /^unknown(\s+user)?(\s*\(.+\))?$/i.test(trimmed);
  }

  _getKnownUserNameById(userId) {
    if (!userId) return '';

    const profile = this.userProfiles && (this.userProfiles[userId] || this.userProfiles[String(userId)]);
    const fromProfile = profile && (profile.displayName || profile.userName || profile.username);
    if (fromProfile) return fromProfile;

    const active = this.activeUsers && (this.activeUsers[userId] || this.activeUsers[String(userId)]);
    const fromActive = active && (active.userName || active.displayName || active.username);
    return fromActive || '';
  }

  _resolveMessageAuthorName(msg) {
    const userId = String((msg && msg.userId) || '');
    const guestName = this._guestNameFromId(userId);
    const rawMsgName = msg && typeof msg.userName === 'string' ? msg.userName.trim() : '';
    const msgName = this._isUnknownName(rawMsgName) ? '' : rawMsgName;
    const knownName = this._getKnownUserNameById(userId);

    const baseName = guestName ? (msgName || guestName) : (knownName || msgName || 'Unknown User');
    return this.resolveName(baseName) || baseName;
  }

  resolveName(originalName) {
    if (!this.userNameMappings || !originalName) return originalName;
    let currentName = originalName;
    let resolvedName = this.userNameMappings[currentName];
    let depth = 0; // safety break for circular dependencies
    while (resolvedName && depth < 10) {
        currentName = resolvedName;
        // Check for the next name in the chain
        resolvedName = this.userNameMappings[currentName];
        depth++;
    }
    return currentName;
  }

  getUserInfo(userId, userName) {
    // Check all possible sources for user info (avatar, color)
    let avatar = null;
    let color = null;

    // 1. Check userProfiles (site_users) by ID
    if (this.userProfiles && userId) {
      const profile = this.userProfiles[userId] || this.userProfiles[String(userId)];
      if (profile) {
        avatar = profile.avatar;
        color = profile.color;
        if (avatar && color) return { avatar, color };
      }
    }

    // 2. Check userProfiles by name (case-insensitive)
    if (this.userProfiles && userName) {
      const searchName = userName.toLowerCase();
      const profile = Object.values(this.userProfiles).find(p =>
        (p.username && p.username.toLowerCase() === searchName) ||
        (p.displayName && p.displayName.toLowerCase() === searchName) ||
        (p.userName && p.userName.toLowerCase() === searchName)
      );
      if (profile) {
        avatar = avatar || profile.avatar;
        color = color || profile.color;
        if (avatar && color) return { avatar, color };
      }
    }

    // 3. Check activeUsers (online presence)
    if (this.activeUsers && userId) {
      const user = this.activeUsers[userId] || this.activeUsers[String(userId)];
      if (user) {
        avatar = avatar || user.avatar;
        color = color || user.color;
        if (avatar && color) return { avatar, color };
      }
    }
    // 4. Check activeUsers by name
    if (this.activeUsers && userName) {
      const searchName = userName.toLowerCase();
      const user = Object.values(this.activeUsers).find(u =>
        (u.userName && u.userName.toLowerCase() === searchName) ||
        (u.displayName && u.displayName.toLowerCase() === searchName)
      );
      if (user) {
        avatar = avatar || user.avatar;
        color = color || user.color;
      }
    }

    return { avatar, color };
  }

